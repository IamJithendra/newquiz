package com.infinitepower.newquiz.model

/**
 * This enum class is used to determine which game mode the user is playing.
 */
enum class GameMode {
    /**
     * Multiple choice game mode.
     */
    MULTI_CHOICE,

    /**
     * Wordle game mode.
     */
    WORDLE,

    /**
     * Comparison game mode.
     */
    COMPARISON_QUIZ
}